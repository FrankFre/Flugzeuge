
public class Flugzeug {

	// Instanzvariablen
	private float spannw;
	private int sitze;

	//Konstruktor
	public Flugzeug(float spannw, int sitze) {
		this.spannw = spannw;
		this.sitze = sitze;
	
	}
	

		//Instanzmethoden
	public float getSpannw() {
		return spannw;
	}

	public int getSitze() {
		return sitze;
	}

}
